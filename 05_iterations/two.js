// Using while loops

let i = 0;
while(i<=20){
    console.log(` The value is ${i}`);
    i+=2
}

let myarr = ['gyan', 'books', 'muscles', 'fluencies']
let arr = 0
while(myarr.length > arr ){
    console.log(`Indexing the element ${myarr[arr]}`)
    arr+=1;
}

//do while loop

let j = 0;
do {
    console.log(`The value is ${j}`);
    j+=2
} while(j<=20)